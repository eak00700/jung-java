public class Q1503 {
    public static int mulInt(int a, int b) {
        return a*b;
        
    }
    public static double mulDouble(double a, double b) {
    return a*b;        
    }
    public static void main(String[] args) {
        System.out.println(mulInt(3, 4));
        System.out.println(mulDouble(3.5, 7));
        
    }
    
}
