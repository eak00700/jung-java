package day15;

public class Q1503_변형 {
    public static int mul(int a, int b){
        return a*b;
    }
    public static double mul(double a, double b) {
        return a*b;
    }
    public static void main(String[] args) {
        System.out.println(mul(3,4));
        System.out.println(mul(3.5, 7));
    }
    
}
